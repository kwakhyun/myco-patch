def _render_js_ts_probe(
    probe_id: str,
    target_file: str,
    risk: RiskFinding,
    pattern: str,
    spore: Spore,
    mode: Literal["safe", "aggressive"],
) -> str:
    comment = "//"
    evidence_lines = "\n".join(f"{comment} - {line}" for line in risk.evidence[:6])
    assertion = (
        "assert.ok(source.includes(riskyPattern), "
        "'The original risky pattern is no longer present; refresh or retire this MycoPatch probe.');"
        if mode == "safe"
        else "assert.ok(!source.includes(riskyPattern), "
        "'Aggressive MycoPatch probe failed because the timezone/date-boundary risk marker is still present. Review the generated markdown report before changing application code.');"
    )
    mode_note = (
        "This safe probe is an executable risk marker. It does not import application code."
        if mode == "safe"
        else "AGGRESSIVE PROBE: this test intentionally fails while the risky static pattern is still present."
    )
    marker = "\n".join(
        [
            f"{comment} mycopatch_probe_id: {probe_id}",
            f"{comment} spore: {spore.name}@{spore.version}",
            f"{comment} risk_type: {risk.risk_type}",
            f"{comment} target_file: {target_file}",
            f"{comment} language: {risk.language}",
            f"{comment} mode: {mode}",
            f"{comment} score: {risk.score}",
            f"{comment} confidence: {risk.confidence}",
            f"{comment} reason: {risk.reason}",
            f"{comment} evidence:",
            evidence_lines if evidence_lines else f"{comment} - no line-level evidence captured",
        ]
    )
    return f'''// Generated by MycoPatch.
// {mode_note}
// It reads the target JS/TS source as text and never imports application code.

import test from 'node:test';
import assert from 'node:assert/strict';
import {{ readFileSync }} from 'node:fs';
import {{ dirname, resolve }} from 'node:path';
import {{ fileURLToPath }} from 'node:url';

{marker}

test('mycopatch js/ts timezone boundary {mode} probe', () => {{
  const __dirname = dirname(fileURLToPath(import.meta.url));
  const repoRoot = resolve(__dirname, '../../..');
  const target = resolve(repoRoot, {json.dumps(target_file)});
  const source = readFileSync(target, 'utf8');
  const riskyPattern = {json.dumps(pattern)};

  assert.ok(riskyPattern, 'MycoPatch probe did not have a clear static pattern to check.');
  {assertion}
}});
'''


