def _slugify(path: str) -> str:
    slug = re.sub(r"[^a-zA-Z0-9]+", "_", path).strip("_").lower()
    return slug[:80] or "repo"


def _probe_prefix(risk: RiskFinding) -> str:
    if risk.language in {"javascript", "typescript"}:
        return "js_timezone_boundary"
    if risk.risk_type == "timezone_boundary":
        return "timezone_boundary"
    return f"python_{risk.risk_type}"


def _ensure_probe_conftest(generated_tests: Path) -> None:
    conftest = generated_tests / "conftest.py"
    if conftest.exists():
        content = conftest.read_text(encoding="utf-8")
        if "mycopatch_aggressive_probe" not in content:
            conftest.write_text(_probe_conftest_content(), encoding="utf-8")
        return
    conftest.write_text(_probe_conftest_content(), encoding="utf-8")


def _probe_conftest_content() -> str:
    return '''"""Pytest configuration for generated MycoPatch probes."""


def pytest_configure(config):
    config.addinivalue_line(
        "markers",
        "mycopatch_probe: generated MycoPatch probe tests",
    )
    config.addinivalue_line(
        "markers",
        "mycopatch_aggressive_probe: generated probes that may intentionally fail",
    )
'''


def _primary_pattern(risk: RiskFinding) -> str:
    for item in risk.evidence_items:
        source_pattern = _source_pattern(item.pattern)
        if source_pattern:
            return source_pattern
    if risk.risk_type in {"mutable_default_argument", "broad_exception_swallow"} and risk.evidence_items:
        return risk.evidence_items[0].snippet
    text = "\n".join(risk.evidence)
    for pattern in ["datetime.utcnow", "date.today", "datetime.now", "datetime(", "replace(tzinfo"]:
        if pattern in text:
            return pattern
    return ""


def _source_pattern(pattern: str) -> str:
    return {
        "datetime.utcnow()": "datetime.utcnow",
        "date.today()": "date.today",
        "datetime.now()": "datetime.now",
        "datetime(...)": "datetime(",
        "replace(tzinfo=...)": "replace(tzinfo",
        "new Date()": "new Date(",
        'new Date("YYYY-MM-DD")': "new Date(",
        "Date.now()": "Date.now",
        "Date.parse(...)": "Date.parse",
        "getDate()": ".getDate(",
        "getMonth()": ".getMonth(",
        "getFullYear()": ".getFullYear(",
        "getHours()": ".getHours(",
        "getMinutes()": ".getMinutes(",
        "getSeconds()": ".getSeconds(",
        "setDate()": ".setDate(",
        "setMonth()": ".setMonth(",
        "setFullYear()": ".setFullYear(",
        "setHours()": ".setHours(",
        "setMinutes()": ".setMinutes(",
        "setSeconds()": ".setSeconds(",
    }.get(pattern, "")


def _write_aggressive_report(
    generated_tests: Path,
    probe_path: Path,
    risk: RiskFinding,
    pattern: str,
) -> Path:
    report_path = probe_path.with_suffix(".md")
    evidence = "\n".join(f"- `{item}`" for item in risk.evidence[:8])
    steps = "\n".join(f"- {step}" for step in risk.recommended_review_steps)
    report_path.write_text(
        f"""# MycoPatch Aggressive Probe Report

- Probe: `{probe_path.name}`
- Target file: `{risk.file_path}`
- Risk type: `{risk.risk_type}`
- Score: {risk.score}
- Confidence: {risk.confidence}
- Static pattern checked: `{pattern or 'none'}`

## Why This Probe May Fail

This aggressive probe intentionally fails while the static risk marker remains
in the target file. It is evidence for human review, not an automatic patch.

## Evidence

{evidence if evidence else "- No line-level evidence captured."}

## Recommended Human Review Steps

{steps if steps else "- Confirm intended behavior and add a regression test."}

## Safety Boundary

The generated test reads the target file only. It does not import application
code, modify source files, use the network, or write outside
`.myco/probes/generated_tests/`.
""",
        encoding="utf-8",
    )
    return report_path
